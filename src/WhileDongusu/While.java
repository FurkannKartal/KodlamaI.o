package WhileDongusu;

public class While {
    public static void main(String[] args) {

        int sayi =9;

        while(sayi<10){
            System.out.println(sayi);
            for (int i = 0; i < 50; i++) {
                System.out.println(sayi);
            }
            sayi=sayi+1;

        }

    }
}
