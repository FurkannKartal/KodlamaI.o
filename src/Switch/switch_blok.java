package Switch;

public class switch_blok {

    public static void main(String[] args) {
        char grade ='C';

        switch (grade){

            case 'A':
                System.out.println("Mukemmel");
                break;
            case 'B':
                System.out.println("Iyı");
                break;
            case 'C':
                System.out.println("Gectiniz");
                break;
            case 'D':
                System.out.println("Şartlı geçtiniz");
                break;
            case 'F':
                System.out.println("Kaldınız");



        }
    }
}
